    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        carregarUsuarios();
        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent e){
                if(e.getClickCount() == 2 && jTable1.getSelectedRow() != -1) {
                    EditarUsuario EU = new EditarUsuario();
                    
                    String OldName = (String)jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0);
                    String Oldpass = new GerenciarUsuarios().senha(OldName);
                    
                    EU.nome.setText(OldName);
                    EU.password.setText(Oldpass);
                    EU.setVisible(true);
                    EU.jOkBtn.addMouseListener(new MouseAdapter() {
                        @Override
                        public void mousePressed(MouseEvent e1) {
                            String Nnome = EU.nome.getText();
                            char[] NPass = EU.password.getPassword();
                            new GerenciarUsuarios().EditarUsuario(OldName, Nnome, NPass);
                            DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                            for(int i = dm.getRowCount() - 1; i >=0; i--)
                                dm.removeRow(i);
                            carregarUsuarios();
                            EU.dispose();
                        }
                    });
                    EU.delbtn.addMouseListener(new MouseAdapter() {
                        @Override
                        public void mousePressed(MouseEvent e1) {
                            new GerenciarUsuarios().DeletarUsuario(EU.nome.getText());
                            DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                            for(int i = dm.getRowCount() - 1; i >=0; i--)
                                dm.removeRow(i);
                            carregarUsuarios();
                            EU.dispose();
                        }
                    });
                }
            }
        });
    }//GEN-LAST:event_formWindowOpened

    private void carregarUsuarios() {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] usr = new GerenciarUsuarios().getAllUsers().toArray();
        for(int i = 0; i < usr.length; i++)
            model.insertRow(i, new Object[] { usr[i]});
    }
